func mbInsertRouteRMs(ctx context.Context, tx *sql.Tx, seqID, parentProductSysID int64, rows []VersionRow, actorUserID string) error {
	const q = `
		INSERT INTO cost_route_rm
			(crm_seq_id, crm_parent_product_sys_id, crm_rm_product_sys_id, crm_rm_group_code,
			 crm_rm_type, crm_route_rm_ratio, crm_created_by, crm_updated_by)
		VALUES ($1, $2, $3, $4, $5, $6, $7, $7)`
	for _, row := range rows {
		pct, err := strconv.ParseFloat(row.CompositionPct, 64)
		if err != nil {
			return fmt.Errorf("mb_autogen: parse composition pct: %w", err)
		}
		if pct == 0 {
			continue
		}
		ratio := pct / 100

		var rmType string
		var rmProductSysID *int64
		var rmGroupCode *string
		if row.SourceType == mbcomposition.SourceTypeMB {
			refProductSysID, resolveErr := mbResolveRefProductSysID(ctx, tx, row.MbRefMbhID)
			if resolveErr != nil {
				return resolveErr
			}
			rmType = costroute.RmTypeProduct
			rmProductSysID = &refProductSysID
		} else {
			rmType = costroute.RmTypeGroup
			groupCode := row.GroupCode
			rmGroupCode = &groupCode
		}

		if _, err := tx.ExecContext(ctx, q, seqID, parentProductSysID, rmProductSysID, rmGroupCode, rmType, ratio, actorUserID); err != nil {
			return fmt.Errorf("mb_autogen: insert cost_route_rm: %w", err)
		}
	}
	return nil
}

func mbResolveRefProductSysID(ctx context.Context, tx *sql.Tx, refMbhID string) (int64, error) {
	const q = `SELECT mbh_cost_product_id FROM mst_mb_head WHERE mbh_id = $1 AND deleted_at IS NULL`
	var productSysID int64
	if err := tx.QueryRowContext(ctx, q, refMbhID).Scan(&productSysID); err != nil {
		return 0, fmt.Errorf("mb_autogen: resolve nested MB cost product: %w", err)
	}
	return productSysID, nil
}

// mbOutputParamCodes are the 7 F_MB_* formulas' result_param_codes (migration 000452). They must
// be registered as CAPP rows (no CPP value — they are computed, not filled) so that LoadFormulas'
// cost_product_applicable_param join actually surfaces these formulas for the auto-generated MB
// cost product; without a CAPP row a formula is invisible to the calc engine regardless of it
// being seeded in mst_formula.
var mbOutputParamCodes = []string{
	"MB_RM_COST", "MB_WASTE_VAL", "MB_NET_PROD", "MB_FIXED_TOTAL",
	"MB_COST_OTHERS", "MB_CONV_COST", "MB_FINAL_COST",
}

// mbFreezeCostParams resolves the 11 mst_parameter IDs and upserts CAPP then CPP rows from the
// entity's already-frozen mbh_param_* values (never a runtime mst_mb_param lookup, except for the
// two picklist params whose option codes must be translated to numeric magnitudes here), then
// registers CAPP-only rows for the 7 F_MB_* formula output params so the calc engine picks them up.
func mbFreezeCostParams(ctx context.Context, tx *sql.Tx, productSysID int64, entity *mbhead.Entity, actorUserID string) error {
	throughputNumeric, err := mbResolveOptionNumericValue(ctx, tx, mbpCodeThroughputPerHour, entity.ParamThroughputPerHour())
	if err != nil {
		return err
	}
	noProcessNumeric, err := mbResolveOptionNumericValue(ctx, tx, mbpCodeNoOfProcess, entity.ParamNoOfProcess())
	if err != nil {
		return err
	}
	values := mbBuildParamValues(entity, throughputNumeric, noProcessNumeric)

	capp := make([]costproductparameter.CAPPUpsertInput, 0, len(values)+len(mbOutputParamCodes))
	cpp := make([]costproductparameter.CPPUpsertInput, 0, len(values))
	now := entity.CreatedAt()
	for _, v := range values {
		paramID, resolveErr := mbResolveParamID(ctx, tx, v.code)
		if resolveErr != nil {
			return resolveErr
		}
		if shapeErr := costproductparameter.EnsureValueShape(v.dataType, v.valueNumericStr, v.valueTextStr, v.valueFlag); shapeErr != nil {
			return fmt.Errorf("mb_autogen: param %s: %w", v.code, shapeErr)
		}
		capp = append(capp, costproductparameter.CAPPUpsertInput{ProductSysID: productSysID, ParamID: paramID, IsRequired: true})
		numeric, parseErr := mbParseOptionalFloat(v.valueNumericStr)
		if parseErr != nil {
			return parseErr
		}
		cpp = append(cpp, costproductparameter.CPPUpsertInput{
			ProductSysID: productSysID, ParamID: paramID,
			ValueNumeric: numeric, ValueText: v.valueTextStr, ValueFlag: v.valueFlag,
			FilledAt: now, FilledBy: actorUserID,
		})
	}
	for _, code := range mbOutputParamCodes {
		paramID, resolveErr := mbResolveParamID(ctx, tx, code)
		if resolveErr != nil {
			return resolveErr
		}
		capp = append(capp, costproductparameter.CAPPUpsertInput{ProductSysID: productSysID, ParamID: paramID, IsRequired: false})
	}

	if _, _, err := upsertCAPPBatch(ctx, tx, capp, actorUserID, now); err != nil {
		return fmt.Errorf("mb_autogen: upsert capp: %w", err)
	}
	if _, _, err := upsertCPPBatch(ctx, tx, cpp, actorUserID, now); err != nil {
		return fmt.Errorf("mb_autogen: upsert cpp: %w", err)
	}
	return nil
}

type mbParamValue struct {
	code            string
	dataType        string
	valueNumericStr *string
	valueTextStr    *string
	valueFlag       *bool
}

func mbBuildParamValues(entity *mbhead.Entity, throughputNumeric, noProcessNumeric *string) []mbParamValue {
	isBoughtout := entity.IsBoughtout()
	compositionVersion := strconv.FormatInt(int64(entity.CurrentVersion()), 10)
	machineFixedTotal := entity.MachineFixedTotal()
	if machineFixedTotal == nil {
		// No mbh_machine_id FK / machine picker exists yet (PRD Pattern C not wired) — 0 is the
		// placeholder snapshot until machine selection lands, not a validation failure.
		zero := "0"
		machineFixedTotal = &zero
	}

	return []mbParamValue{
		{code: paramMBWaste, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamWaste()},
		{code: paramMBQualityLoss, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamQualityLoss()},
		{code: paramMBEfficiency, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamEfficiency()},
		{code: paramMBDevExpense, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamDevExpense()},
		{code: paramMBPacking, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamPacking()},
		{code: paramMBProdPerDay, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: entity.ParamMBProdPerDay()},
		{code: paramMBThroughput, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: throughputNumeric},
		{code: paramMBNoProcess, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: noProcessNumeric},
		{code: paramIsBoughtout, dataType: string(costproductparameter.DataTypeBoolean), valueFlag: &isBoughtout},
		{code: paramMachineFixedTotal, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: machineFixedTotal},
		{code: paramCompositionVer, dataType: string(costproductparameter.DataTypeNumber), valueNumericStr: &compositionVersion},
	}
}

// mbResolveOptionNumericValue translates a frozen mst_mb_param picklist option code (e.g.
// throughput_per_hour="B") into its mst_mb_param_option.mbpo_numeric_value magnitude, which is
// what CPP must store for these two params (data_type NUMBER). Returns nil if code is empty.
func mbResolveOptionNumericValue(ctx context.Context, tx *sql.Tx, paramCode, optionCode string) (*string, error) {
	if optionCode == "" {
		return nil, nil //nolint:nilnil // absent selection is a legitimate "not set" signal, not an error
	}
	const q = `
		SELECT mbpo_numeric_value FROM mst_mb_param_option
		WHERE mbpo_mbp_code = $1 AND mbpo_code = $2 AND deleted_at IS NULL`
	var numeric float64
	if err := tx.QueryRowContext(ctx, q, paramCode, optionCode).Scan(&numeric); err != nil {
		return nil, fmt.Errorf("mb_autogen: resolve mst_mb_param_option %s/%s: %w", paramCode, optionCode, err)
	}
	v := strconv.FormatFloat(numeric, 'f', -1, 64)
	return &v, nil
}

func mbParseOptionalFloat(s *string) (*float64, error) {
	if s == nil {
		return nil, nil //nolint:nilnil // absent value is a legitimate "not set" signal, not an error
	}
	v, err := strconv.ParseFloat(*s, 64)
	if err != nil {
		return nil, fmt.Errorf("mb_autogen: parse numeric param: %w", err)
	}
	return &v, nil
}

func mbResolveParamID(ctx context.Context, tx *sql.Tx, code string) (uuid.UUID, error) {
	const q = `SELECT id FROM mst_parameter WHERE param_code = $1 AND deleted_at IS NULL`
	var id uuid.UUID
	if err := tx.QueryRowContext(ctx, q, code).Scan(&id); err != nil {
		return uuid.Nil, fmt.Errorf("mb_autogen: resolve mst_parameter %s: %w", code, err)
	}
	return id, nil
}

func mbWriteBackCostProduct(ctx context.Context, tx *sql.Tx, id uuid.UUID, productSysID int64, actorUserID string) error {
	const q = `
		UPDATE mst_mb_head
		SET mbh_cost_product_id = $2, mbh_cost_generated_at = NOW(), mbh_cost_generated_by = $3
		WHERE mbh_id = $1 AND deleted_at IS NULL`
	if _, err := tx.ExecContext(ctx, q, id, productSysID, actorUserID); err != nil {
		return fmt.Errorf("mb_autogen: write back cost product: %w", err)
	}
	return nil
}
