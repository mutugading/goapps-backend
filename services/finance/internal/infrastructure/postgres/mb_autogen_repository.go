// TransitionWithAutoGen performs the same work as Transition, then — only when toState is
// StatusValidated and entity's cost product has not already been generated — auto-generates the
// linked cost_product_master/cost_route_*/CAPP/CPP rows per design addendum §10.2. All writes
// (transition + auto-gen) commit or roll back together.
func (r *MBHeadRepository) TransitionWithAutoGen(ctx context.Context, id uuid.UUID, fromState, toState string, currentVersion int32, stateReason, actorUserID string, params *mbhead.ParamSnapshot, entity *mbhead.Entity) error {
	return r.db.Transaction(ctx, func(tx *sql.Tx) error {
		if err := r.updateEntryStatusTx(ctx, tx, id, toState, currentVersion, stateReason, params); err != nil {
			return err
		}
		if err := r.insertWorkflowLogTx(ctx, tx, id, fromState, toState, actorUserID, stateReason, currentVersion); err != nil {
			return err
		}
		if toState != mbhead.StatusValidated {
			return nil
		}
		if err := r.compositionRepo.SnapshotVersion(ctx, tx, id.String(), currentVersion, actorUserID); err != nil {
			return err
		}
		if entity.CostProductID() != 0 {
			return nil
		}
		return r.autoGenCostProduct(ctx, tx, id, currentVersion, actorUserID, entity)
	})
}

// autoGenCostProduct runs the 6-step auto-gen write sequence (design addendum §10.2) inside the
// caller's transaction.
func (r *MBHeadRepository) autoGenCostProduct(ctx context.Context, tx *sql.Tx, id uuid.UUID, version int32, actorUserID string, entity *mbhead.Entity) error {
	typeID, err := mbResolveCostProductTypeID(ctx, tx)
	if err != nil {
		return err
	}
	productSysID, err := mbInsertCostProductMaster(ctx, tx, typeID, entity, actorUserID)
	if err != nil {
		return err
	}
	headID, err := mbInsertRouteHead(ctx, tx, productSysID, actorUserID)
	if err != nil {
		return err
	}
	seqID, err := mbInsertRouteSeq(ctx, tx, headID, productSysID, actorUserID)
	if err != nil {
		return err
	}
	rows, err := r.compositionRepo.ListVersionByMbhIDAndVersion(ctx, tx, id.String(), version)
	if err != nil {
		return err
	}
	if err := mbInsertRouteRMs(ctx, tx, seqID, productSysID, rows, actorUserID); err != nil {
		return err
	}
	if err := mbFreezeCostParams(ctx, tx, productSysID, entity, actorUserID); err != nil {
		return err
	}
	return mbWriteBackCostProduct(ctx, tx, id, productSysID, actorUserID)
}

func mbResolveCostProductTypeID(ctx context.Context, tx *sql.Tx) (int32, error) {
	const q = `SELECT cpt_type_id FROM cost_product_type WHERE cpt_type_code = $1 AND cpt_is_active = TRUE`
	var typeID int32
	if err := tx.QueryRowContext(ctx, q, mbCostProductTypeCode).Scan(&typeID); err != nil {
		return 0, fmt.Errorf("mb_autogen: resolve cost_product_type: %w", err)
	}
	return typeID, nil
}

func mbInsertCostProductMaster(ctx context.Context, tx *sql.Tx, typeID int32, entity *mbhead.Entity, actorUserID string) (int64, error) {
	const codeQ = `SELECT generate_cost_product_code($1)`
	var code string
	if err := tx.QueryRowContext(ctx, codeQ, typeID).Scan(&code); err != nil {
		return 0, fmt.Errorf("mb_autogen: generate cost product code: %w", err)
	}

	const insertQ = `
		INSERT INTO cost_product_master
			(cpm_product_code, cpm_product_type_id, cpm_product_name, cpm_source, cpm_is_locked,
			 cpm_created_by, cpm_updated_by)
		VALUES ($1, $2, $3, $4, TRUE, $5, $5)
		RETURNING cpm_product_sys_id`
	var productSysID int64
	err := tx.QueryRowContext(ctx, insertQ, code, typeID, entity.MBCosting(), mbCostProductSource, actorUserID).Scan(&productSysID)
	if err != nil {
		return 0, fmt.Errorf("mb_autogen: insert cost_product_master: %w", err)
	}
	return productSysID, nil
}

